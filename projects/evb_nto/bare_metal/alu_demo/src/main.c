static void run_div_test(void)
{
    uint32_t ret = TEST_SUCCESS;

    uint32_t A[128]       = { 0 }; // 4096bit, 512byte
    uint32_t B[128]       = { 0 }; // Increased size to be safe
    uint32_t Q[128]       = { 0 };
    uint32_t R[128]       = { 0 };
    uint32_t expectQ[128] = { 0 };
    uint32_t expectR[128] = { 0 };
    uint32_t i;
    uint32_t qlen, rlen, xlen, ylen;
    uint32_t testcnt = sizeof(dividend) / sizeof(dividend[0]);

    printf("==== Starting ALU Division Test ====\r\n");

    for (i = 0; i < testcnt; i++)
    {
        memset(A, 0, sizeof(A));
        memset(B, 0, sizeof(B));
        memset(Q, 0, sizeof(Q));
        memset(R, 0, sizeof(R));
        memset(expectQ, 0, sizeof(expectQ));
        memset(expectR, 0, sizeof(expectR));

        xlen = bnu_hex2buf_le(dividend[i], (uint8_t *)A, sizeof(A));
        ylen = bnu_hex2buf_le(divisor[i], (uint8_t *)B, sizeof(B));

        /* The logic from source:
           xlen = (xlen + 7) / 8 * 2;
           ylen = (ylen + 7) / 8 * 2;
        */
        xlen = (xlen + 7) / 8 * 2;
        ylen = (ylen + 7) / 8 * 2;

        bnu_hex2buf_le(expectquotient[i], (uint8_t *)expectQ, sizeof(expectQ));
        bnu_hex2buf_le(expectremainder[i], (uint8_t *)expectR, sizeof(expectR));

        bndivision_le(A, xlen, B, ylen, Q, &qlen, R, &rlen);

        printf("SW floor division [%" PRIx32 "]: ExpQl = %" PRIx32 ", ExpRl = %" PRIx32 "; Ql = %" PRIx32 ", Rl = %" PRIx32 "\n", i, bnu_get_nzw_le(expectQ, sizeof(expectQ) / 4),
               bnu_get_nzw_le(expectR, sizeof(expectR) / 4), qlen, rlen);
        bnu_print_mem("A: ", A, sizeof(A));
        bnu_print_mem("B: ", B, sizeof(B));
        bnu_print_mem("Expect Q: ", (uint8_t *)expectQ, qlen * 4);
        bnu_print_mem("Q = A/B:  ", (uint8_t *)Q, qlen * 4);
        bnu_print_mem("Expect R: ", (uint8_t *)expectR, rlen * 4);
        bnu_print_mem("R = A\%B: ", (uint8_t *)R, rlen * 4);

        if ((memcmp(Q, expectQ, qlen * 4) == 0) && (memcmp(R, expectR, rlen * 4) == 0))
        {
            printf("SW floor division [%" PRIx32 "]: %" PRIx32 "bits/%" PRIx32 "bits Success\r\n", i, bnu_get_msb_le(A, xlen / 4) + 1, bnu_get_msb_le(B, ylen / 4) + 1);
        }
        else
        {
            ret |= TEST_FAILURE;
            if (memcmp(Q, expectQ, qlen * 4) != 0)
            {
                printf("SW floor division [%" PRIx32 "]: Quotient Error\r\n", i);
            }
            if (memcmp(R, expectR, rlen * 4) != 0)
            {
                printf("SW floor division [%" PRIx32 "]: Remainder Error\r\n", i);
            }
            // bnu_print_mem("A: ", (uint8_t*)A, bnu_get_nzw_le(A, xlen/4)*4);
            // bnu_print_mem("B: ", (uint8_t*)B, bnu_get_nzw_le(B, ylen/4)*4);
            // bnu_print_mem("Expect Q: ", (uint8_t*)expectQ, qlen*4);
            // bnu_print_mem("Q = A/B:  ", (uint8_t*)Q, qlen*4);
            // bnu_print_mem("Expect R: ", (uint8_t*)expectR, rlen*4);
            // bnu_print_mem("R = A%B: ", (uint8_t*)R, rlen*4);
        }
    }

    if (ret == TEST_SUCCESS)
    {
        printf("==== All ALU Division Tests Passed! ====\r\n");
    }
    else
    {
        printf("==== Some ALU Division Tests Failed! ====\r\n");
    }
}

